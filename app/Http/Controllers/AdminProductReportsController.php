	    public function getIndex(NewRequest $request) {
		  //First, Add an auth
		   if(!CRUDBooster::isView()) CRUDBooster::denyAccess();

		   //Create your own query 
		   $data = [];
		   $data['page_title'] = 'Product Reports';
		   $data['result'] = (new ProductRepository)->getProductReport($request);

		   $data['keys'] = [];
		   foreach (collect($data['result'][0]) as $key => $value) {
		   		$data['keys'][] = $key;
		   }

		   $data['attribute'] = $request->has('attr') ? $request->input('attr') : $data['keys'];
		   $data['designer'] = $request->has('designer') ? $request->input('designer') : null;
		   $data['name'] = $request->has('name') ? $request->input('name') : 'report-product-'.date('YmdHIs');
		   $data['date_create'] = $request->has('date_create') ? $request->input('date_create') : null;
		   $data['designers'] = (new DesignerRepository)->getDesigners();

		   //Create a view. Please use `cbView` method instead of view method from laravel.
		   $this->cbView('admin.product_reports',$data);
		}

		public function export(NewRequest $request)
		{
			try {

				if(!CRUDBooster::isCreate()) {
					throw new Exception("You is not admin", 1);
				}

				if ($download = $request->input('download')) {
	                $get = storage_path('csv/'.$download);
	                return response()->download($get)->deleteFileAfterSend(true);
	            }

				$result = (new ProductRepository)->getProductReport($request);

				$keys = [];
			   	foreach (collect($result[0]) as $key => $value) {
			   		$keys[] = $key;
			   	}

				$data = [
	                'type' => $request->has('type_request') ? $request->input('type_request') : 'new',
	                'filename' => $request->has('filename') ? $request->input('filename') : 'report-product-'.date('YmdHis').'.csv',
	                'attribute' => $request->has('attr') ? $request->input('attr') : $keys,
	                'page' => $request->has('page') ? $request->input('page') : 1,
	                'no' => $request->has('no') ? $request->input('no') : '1'
	            ];

				$reports = (new ReportService)->export($result, $data);

				return response()->json([
	                'status' => 'ok',
	                'message' => $reports,
	            ], 200);
			} catch (\Exception $e) {
				logger($e);
				return response()->json([
	                'status' => 'error',
	                'message' => $e->getMessage().' '.$e->getLine(),
	            ], 500);
			}
		}

